function mortality(model; m = 1 / 45, J = 120)
    j₀ = 0
    j_dim = Dim{:j}(j₀:J)

    if model == :perpetual_youth
        return DimVector([fill(m, J); 1.0], j_dim)
    elseif model == :lifecycle
        return 1 .- p_surv₀[j = At(j₀:J)]
    else
        throw(ArgumentError("model must be either :perpetual_youth or :lifecycle"))
    end
end

function income_profile(J, JR)
    @assert JR < J

    y = [3e-06 * j^3 - 0.0012 * j^2 + 0.0589 * j + 0.9503 for j in 1:JR]
    y = [y; fill(y[end], J - JR)]

    return DimArray(y, Dim{:j}(0:J-1), name = :y)
end

function simple_income_profile(J, JR; y = 1.0, yR = 0.0)
    profile = [fill(y, JR); fill(yR, J - JR)]
    return DimArray(profile, Dim{:j}(0:J-1), name = :y)
end
