function pmf(m; births = nothing)
    j_dim = DimensionalData.dims(m, :j)
    _births_ = isnothing(births) ? 1.0 : births

    dist = cumprod([_births_; (1 .- m)])[begin:end-1]
    dist = DimVector(dist, j_dim, name = :pmf)

    if isnothing(births)
        dist .= dist ./ sum(dist)
    end

    return dist
end

