@testset "Structural checks" begin
    @test pe_py.sol.sim_df.a_next[end] == 0.0
    @test sol_egm_py.sim_df.a_next[end] == 0.0

    lhs_vfi = pe_py.sol.sim_df.c .+ (1 .- pe_py.sol.sim_df.m) .* pe_py.sol.sim_df.a_next
    rhs_vfi = (1 .+ pe_py.sol.sim_df.r) .* pe_py.sol.sim_df.a .+ pe_py.sol.sim_df.w .* pe_py.sol.sim_df.y
    @test all(isapprox.(lhs_vfi, rhs_vfi; atol = 1e-12, rtol = 1e-12))

    m_egm = [par_py.m[j = At(j)] for j in sol_egm_py.sim_df.j]
    y_egm = [par_py.y[j = At(j)] for j in sol_egm_py.sim_df.j]
    lhs_egm = sol_egm_py.sim_df.c .+ (1 .- m_egm) .* sol_egm_py.sim_df.a_next
    rhs_egm = (1 .+ prices_py.r) .* sol_egm_py.sim_df.a .+ prices_py.w .* y_egm
    @test all(isapprox.(lhs_egm, rhs_egm; atol = 1e-10, rtol = 1e-10))

    for j in 0:par_py.J
        policy_j = collect(pe_py.sol.out_t.policy[j = At(j)])
        @test all(diff(policy_j) .>= 0)
    end

    @test sum(pmf(par_py.m)) ≈ 1.0
    @test sum(pmf(par_life.m)) ≈ 1.0

    K = K_guess_life
