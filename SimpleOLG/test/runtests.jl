function analytical_solution((; γ, β, J), (; r, w), (; y, a₀))
    inc = w * y
    wealth = sum((1 / (1 + r))^j * inc for j in 0:J-1) + (1 + r) * a₀

    c₀ = wealth / sum((1 / (1 + r))^j * (β * (1 + r))^(j / γ) for j in 0:J-1)

    c = [c₀ * (β * (1 + r))^(j / γ) for j in 0:J-1]
    a_next = Float64[]

    a = a₀
    for cⱼ in c
        a = (1 + r) * a + inc - cⱼ
        push!(a_next, a)
    end

    return (; c, a_next)
end

