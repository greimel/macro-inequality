    @test interest_rate(K, par_life) + par_life.δ ≈ par_life.α * Y / K
    @test wage(K, par_life) ≈ (1 - par_life.α) * Y / par_life.L
    @test inverse_interest_rate(interest_rate(K, par_life), par_life) ≈ K

