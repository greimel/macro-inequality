    K_large = 8.0
    pe_large = partial_equilibrium(
        par_life,
        a_grid_sign,
        (; K_guess = K_large, r = interest_rate(K_large, par_life), w = wage(K_large, par_life));
        solution_method = EGM(),
    )
    @test pe_small.ζ > 0
    @test pe_large.ζ < 0
end

@testset "Analytical finite-horizon special case" begin
    J = 10
    β = 0.9
    γ = 2.0
    r = 1 / β - 1 + 0.09
    w = 1.0
    y = simple_income_profile(J, J; y = 1.0)
    par = get_par(; demo = :perpetual_youth, mm = 0.0, y, β, γ, a̲ = 0.0)
    a_grid = range(0.0, 1.1, length = 1200)

    analytical = analytical_solution((; γ, β, J), (; r, w), (; y = 1.0, a₀ = 0.0))
    sol_vfi = solve_backward_forward(VFI(), par, a_grid; r, w, a_init = 0.0)
    sol_egm = solve_backward_forward(EGM(), par, a_grid; r, w, a_init = 0.0)

    @test maximum(abs.(sol_vfi.sim_df.c .- analytical.c)) < 3e-3
    @test maximum(abs.(sol_vfi.sim_df.a_next .- analytical.a_next)) < 3e-3
    @test maximum(abs.(sol_egm.sim_df.c .- analytical.c)) < 2e-3
    @test maximum(abs.(sol_egm.sim_df.a_next .- analytical.a_next)) < 2e-3
end

